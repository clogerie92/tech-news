public class VariableTypes {
    public static void main(String[] args) {
        String name = "Cleopatra";
        int age = 2016 + 69;
        double price = 1999.99;
        boolean truth = true;
        boolean falseness = !truth;

        System.out.println("Her name is" + name + "!");
        System.out.println("Her age is" + age + "!");
        System.out.println("Her price is" + price + "!");
        System.out.println("This is boolean is true: " + truth);
        System.out.println("This is boolean is true: " + falseness);
    }

}
